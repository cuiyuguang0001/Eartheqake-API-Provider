package turui.eartheqake.core.pojo.user;

public class User_group {

    //主键用户编号
    private String id;
    //权限名
    private String name;
    //权限级别
    private String list;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getList() {
        return list;
    }

    public void setList(String list) {
        this.list = list;
    }
}
